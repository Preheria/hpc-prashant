	#define n_threads 4

	unsigned char image[], results[width * height];
	
	typedef struct{
	unsigned char *in;
	unsigned char *out;
	int start,stride,n_pixels;
	} in_out;

	int time_difference(struct timespec *start, struct timespec *finish,
                    long long int *difference) {
  	long long int ds =  finish->tv_sec - start->tv_sec; 
  	long long int dn =  finish->tv_nsec - start->tv_nsec; 

  	if(dn < 0 ) {
    	ds--;
    	dn += 1000000000; 
  	} 
  	*difference = ds * 1000000000 + dn;
  	return !(*difference > 0);
	}

	void detect_edges(unsigned char *in, unsigned char *out) {
		pthread_t *t = malloc(sizeof(pthread_t) * n_threads);
		in_out *inout = malloc(sizeof(in_out) * n_threads);
		
		int i;
		for(i=0;i<n_threads;i++){
			inout[i].start = i;
		  	inout[i].stride = n_threads;
		  	inout[i].n_pixels = width * height;
			inout[i].in = in;
			inout[i].out = out;
		}

		void *detector();
		
		for(i=0;i<n_threads;i++){
		  	pthread_create(&t[i], NULL, detector, &inout[i]);
		}

		for(i=0;i<n_threads;i++){
		  	pthread_join(t[i], NULL);
		}

		free(t);
		free(inout);
	}
	void *detector(in_out *inout)
	{
		
		for(int i=(inout->start);i<inout->n_pixels;i+=(inout->stride)) {
		  int x, y; // the pixel of interest
		  int b, d, f, h; // the pixels adjacent to x,y used for the calculatinoutn
		  int r; // the result of calculate
		  
		  	y = i / width;
		  	x = i - (width * y);

		  if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
		    	results[i] = 0;
		  	}
		  else {
		    	b = i + width;
		    	d = i - 1;
		    	f = i + 1;
		    	h = i - width;

		    	r = (inout->in[i] * 4) + (inout->in[b] * -1) + (inout->in[d] * -1) + (inout->in[f] * -1)
		    	    + (inout->in[h] * -1);

		    if (r > 0) { // if the result is positive this is an edge pixel
		      		inout->out[i] = 255;
		    	} 
		    else {
		      		inout->out[i] = 0;
		    	}
		  	}
		}
	
	}

